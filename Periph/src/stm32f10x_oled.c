/*****************************************************************************
 ? ? ?  : LED_WrDat
 ????  : ?OLED???
 ????  : unsigned char ucData  
 ????  : NONE
 ? ? ?  : NONE
*****************************************************************************/
void LED_WrDat(unsigned char ucData)
{
    unsigned char i = 8;
    GPIO_SetBits(LED_PORT,LED_DC);
    GPIO_ResetBits(LED_PORT,LED_SCL);
    
    while (i--)
    {
        if (ucData & 0x80)
        {
            GPIO_SetBits(LED_PORT,LED_SDA);
        }
        else
        {
            GPIO_ResetBits(LED_PORT,LED_SDA);
        }
        
        GPIO_SetBits(LED_PORT,LED_SCL);
        __nop();//__nop();__nop();
        GPIO_ResetBits(LED_PORT,LED_SCL); 
        ucData <<= 1;    
    }

    return;
}

/*****************************************************************************
 ? ? ?  : LED_WrCmd
 ????  : ?OLED???
 ????  : unsigned char ucCmd  
 ????  : NONE
 ? ? ?  : NONE
*****************************************************************************/
void LED_WrCmd(unsigned char ucCmd)
{
    unsigned char i = 8;
    GPIO_ResetBits(LED_PORT,LED_DC); 
    GPIO_ResetBits(LED_PORT,LED_SCL); 
    while (i--)
    {
        if (ucCmd & 0x80)
        {
            GPIO_SetBits(LED_PORT,LED_SDA);
        }
        else
        {
            GPIO_ResetBits(LED_PORT,LED_SDA); 
        }
        GPIO_SetBits(LED_PORT,LED_SCL);
        __nop();//__nop();__nop();          
        GPIO_ResetBits(LED_PORT,LED_SCL);     
        ucCmd <<= 1;   
    }  

    return;
}


